package com.tt.assignment2;

public class Assignment2_7 {

	public static void main(String[] args) {
		// 7.	You are given two integer number (a=25, b=30), write a program to swap them and display result on the screen

		int a = 25;
		int b = 30;
		int c;
		
		c=a;
		a=b;
		b=c;
		
		System.out.println("Swap value of a is " + a);
		System.out.println("Swap value of b is " + b);
		
	}

}
