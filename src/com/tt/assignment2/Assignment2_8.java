package com.tt.assignment2;

public class Assignment2_8 {

	public static void main(String[] args) {
		// 8.	Write Java Program to display Factorial of Number 7.
		
		int n = 7;
		int result = 1;
		for(int i=1; i <=n; i++){
			result = result * i;
			
			System.out.println( " The Factorial of Number 7 is " + result);
		}
	

	}

}
